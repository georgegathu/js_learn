// ARROW FUNCTIONS
let my_team = function(){
    console.log("Manchester United");
};
my_team();

//This here is an example of an Arrow Function: Example 1   
let trophy = (status)=> {
    console.log("EPL",status);
};
trophy("Winners!");

//This here is an example of an Arrow Function: Example 2(Using backticks)   
