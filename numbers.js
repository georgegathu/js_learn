// Numbers in JS

let radius = 15;
const pi = 3.14; // we use const instead of let because pi does not change (its constant)

//finds area of a circle
let area = pi * radius ** 2
console.log(area);

// Short hand method
radius ++; // adds +1 to the value of radius
console.log(radius);
radius --; //minus -1 to the value of radius
console.log(radius);

radius += 5; // adds: radius + 5
console.log(radius);
radius -= 10; // minus: radius - 10
console.log(radius);

// Concatination
console.log("Value of radius is " + radius + " and the value of pi is " + pi);




